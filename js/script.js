// Wait until the DOM is fully loaded

document.addEventListener("DOMContentLoaded", function () {
  const form = document.querySelector("form");
  const commentsSection = document.querySelector(".pagination").parentNode;

  function timeSince(date) {
    const seconds = Math.floor((new Date() - date) / 1000);

    let interval = Math.floor(seconds / 31536000);
    if (interval > 1) return interval + " years ago";

    interval = Math.floor(seconds / 2592000);
    if (interval > 1) return interval + " months ago";

    interval = Math.floor(seconds / 86400);
    if (interval > 1) return interval + " days ago";

    interval = Math.floor(seconds / 3600);
    if (interval > 1) return interval + " hours ago";

    interval = Math.floor(seconds / 60);
    if (interval > 1) return interval + " minutes ago";

    return Math.floor(seconds) + " seconds ago";
  }

  // Form submission event
  form.addEventListener("submit", function (e) {
    e.preventDefault(); // Prevent the default form submission

    // Capture form inputs
    const nameInput = form.querySelector(".nama").value.trim();
    const messageAlamat = form.querySelector(".alamat").value.trim();
    const messageInput = form.querySelector(".ucapan").value.trim();
    const messageKonfirmasi = form.querySelector(".status").value.trim();
    const timestamp = new Date();
    // Validate inputs
    if (
      !nameInput ||
      !messageAlamat ||
      !messageInput ||
      !messageKonfirmasi ||
      !timestamp
    ) {
      alert("Please fill out all fields.");
      return;
    }

    // Create a new comment element
    const newComment = document.createElement("div");
    // newComment.classList.add("comment");

    newComment.innerHTML = `
    <div class="row justify-content-center">
       <div class="comment col-md-10 col-10">
          <div class="comment-header ">
              <span class="name">${nameInput}</span>
             <span class="time" data-time="${timestamp}">${timeSince(
      new Date(timestamp)
    )}</span>
          </div>
          <div class="comment-body"> 
            ${messageAlamat}
          </div>
          <div class="comment-body"> 
            ${messageInput}
          </div>
          <div class="comment-body"> 
            ${messageKonfirmasi}
          </div>
        </div>
    </div>
      `;

    // Add the new comment to the comments section before the pagination
    commentsSection.insertBefore(
      newComment,
      document.querySelector(".pagination")
    );

    // Clear form fields
    form.reset();
  });

  setInterval(function () {
    const times = document.querySelectorAll(".time");
    times.forEach(function (time) {
      const timestamp = new Date(time.getAttribute("data-time"));
      time.textContent = timeSince(timestamp);
    });
  }, 60000);
});

